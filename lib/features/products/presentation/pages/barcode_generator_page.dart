class _BarcodeItem {
  final String id;
  final String name;
  final String barcodeValue;
  final int qty;
  final bool autoGenerated;

  const _BarcodeItem({
    required this.id,
    required this.name,
    required this.barcodeValue,
    this.qty = 1,
    this.autoGenerated = false,
  });

  _BarcodeItem copyWith({
    String? name,
    String? barcodeValue,
    int? qty,
    bool? autoGenerated,
  }) => _BarcodeItem(
    id: id,
    name: name ?? this.name,
    barcodeValue: barcodeValue ?? this.barcodeValue,
    qty: qty ?? this.qty,
    autoGenerated: autoGenerated ?? this.autoGenerated,
  );
}

class _BarcodeHistoryEntry {
  final String barcodeValue;
  final String name;
  final String type;
  final String updatedAt;

  const _BarcodeHistoryEntry({
    required this.barcodeValue,
    required this.name,
    required this.type,
    required this.updatedAt,
  });

  factory _BarcodeHistoryEntry.fromJson(Map<String, dynamic> json) =>
      _BarcodeHistoryEntry(
        barcodeValue: json['barcodeValue'] as String? ?? '',
        name: json['name'] as String? ?? '',
        type: json['type'] as String? ?? 'code128',
        updatedAt: json['updatedAt'] as String? ?? '',
      );

  Map<String, dynamic> toJson() => {
    'barcodeValue': barcodeValue,
    'name': name,
    'type': type,
    'updatedAt': updatedAt,
  };
}

class BarcodeGeneratorPage extends ConsumerStatefulWidget {
  const BarcodeGeneratorPage({super.key});

  @override
  ConsumerState<BarcodeGeneratorPage> createState() =>
      _BarcodeGeneratorPageState();
}

class _BarcodeGeneratorPageState extends ConsumerState<BarcodeGeneratorPage> {
  static const _historyPrefsKey = 'barcode_generator_history_v1';
  static const _historyLimit = 20;

  final List<_BarcodeItem> _items = [];
  List<_BarcodeHistoryEntry> _history = const [];

  BarcodePdfType _type = BarcodePdfType.code128;
  int _columnsPerRow = 3;
  bool _isPrinting = false;
  bool _isPreviewing = false;
  bool _isSharing = false;
  bool _isHistoryLoading = true;
  String? _selectedItemId;
  String _historyQuery = '';

  int get _totalSlips => _items.fold(0, (s, i) => s + i.qty);

  @override
  void initState() {
    super.initState();
    _loadHistory();
  }

  Future<void> _loadHistory() async {
    final prefs = await SharedPreferences.getInstance();
    final raw = prefs.getString(_historyPrefsKey);
    if (raw == null || raw.isEmpty) {
      if (!mounted) return;
      setState(() => _isHistoryLoading = false);
      return;
    }

    try {
      final decoded = jsonDecode(raw) as List<dynamic>;
      final entries = decoded
          .whereType<Map>()
          .map(
            (e) => _BarcodeHistoryEntry.fromJson(Map<String, dynamic>.from(e)),
          )
          .where(
            (e) => e.barcodeValue.trim().isNotEmpty && e.name.trim().isNotEmpty,
          )
          .toList();
      if (!mounted) return;
      setState(() {
        _history = entries;
        _isHistoryLoading = false;
      });
    } catch (_) {
      if (!mounted) return;
      setState(() => _isHistoryLoading = false);
    }
  }

  Future<void> _persistHistory() async {
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(
      _historyPrefsKey,
      jsonEncode(_history.map((e) => e.toJson()).toList()),
    );
  }

  Future<void> _saveHistoryEntry({
    required String barcodeValue,
    String name = '',
    BarcodePdfType? type,
  }) async {
    final barcode = barcodeValue.trim();
    final trimmedName = name.trim();
    if (barcode.isEmpty || trimmedName.isEmpty) return;

    final entry = _BarcodeHistoryEntry(
      barcodeValue: barcode,
      name: trimmedName,
      type: _barcodeTypeKey(type ?? _type),
      updatedAt: DateTime.now().toIso8601String(),
    );

    final next = [
      entry,
      ..._history.where((e) => e.barcodeValue != barcode),
    ].take(_historyLimit).toList();

    if (!mounted) return;
    setState(() => _history = next);
    await _persistHistory();
  }

  Future<void> _removeHistoryEntry(String barcodeValue) async {
    setState(() {
      _history = _history.where((e) => e.barcodeValue != barcodeValue).toList();
    });
    await _persistHistory();
  }

  Future<void> _clearHistory() async {
    setState(() => _history = const []);
    final prefs = await SharedPreferences.getInstance();
    await prefs.remove(_historyPrefsKey);
  }

  Future<void> _persistCurrentItemsToHistory() async {
    for (final item in _items) {
      await _saveHistoryEntry(barcodeValue: item.barcodeValue, name: item.name);
    }
  }

  List<_BarcodeHistoryEntry> get _filteredHistory {
    final q = _historyQuery.trim().toLowerCase();
    if (q.isEmpty) return _history;
    return _history.where((entry) {
      return entry.name.toLowerCase().contains(q) ||
          entry.barcodeValue.toLowerCase().contains(q) ||
          entry.type.toLowerCase().contains(q);
    }).toList();
  }

  void _addItem({
    String name = '',
    String? barcode,
    bool autoGenerated = true,
  }) {
    final nextBarcode = barcode?.trim().isNotEmpty == true
        ? barcode!.trim()
        : _generateBarcode();
    setState(() {
      final item = _BarcodeItem(
        id: UniqueKey().toString(),
        name: name,
        barcodeValue: nextBarcode,
        autoGenerated: barcode?.trim().isEmpty ?? true ? autoGenerated : false,
      );
      _items.insert(0, item);
      _selectedItemId = item.id;
    });
    unawaited(_saveHistoryEntry(barcodeValue: nextBarcode, name: name));
  }

  void _addFromProduct(ProductModel p) {
    final productBarcode = (p.barcode?.trim().isNotEmpty ?? false)
        ? p.barcode!.trim()
        : _generateBarcode();
    _addItem(
      name: p.productName,
      barcode: productBarcode,
      autoGenerated: p.barcode?.trim().isEmpty ?? true,
    );
  }

  void _addFromHistory(_BarcodeHistoryEntry entry) {
    final duplicateIndex = _items.indexWhere(
      (item) => item.barcodeValue.trim() == entry.barcodeValue.trim(),
    );
    if (duplicateIndex >= 0) {
      _selectItem(_items[duplicateIndex].id);
      _snack('บาร์โค้ดนี้มีอยู่ในรายการแล้ว', error: true);
      return;
    }

    _addItem(
      name: entry.name,
      barcode: entry.barcodeValue,
      autoGenerated: false,
    );
  }

  void _removeItem(String id) => setState(() {
    _items.removeWhere((i) => i.id == id);
    if (_selectedItemId == id) {
      _selectedItemId = _items.isNotEmpty ? _items.first.id : null;
    }
  });

  void _selectItem(String id) {
    if (_selectedItemId == id) return;
    setState(() => _selectedItemId = id);
  }

  void _updateItem(
    String id, {
    String? name,
    String? barcodeValue,
    int? qty,
    bool? autoGenerated,
  }) {
    setState(() {
      final idx = _items.indexWhere((i) => i.id == id);
      if (idx < 0) return;
      _items[idx] = _items[idx].copyWith(
        name: name,
        barcodeValue: barcodeValue,
        qty: qty,
        autoGenerated: autoGenerated,
      );
    });

    final updated = _items.firstWhere((i) => i.id == id);
    if (updated.barcodeValue.trim().isNotEmpty &&
        (barcodeValue != null || name != null)) {
      unawaited(
        _saveHistoryEntry(
          barcodeValue: updated.barcodeValue.trim(),
          name: updated.name,
        ),
      );
    }
  }

  void _regenerateBarcode(String id) {
    final next = _generateBarcode();
    _updateItem(id, barcodeValue: next, autoGenerated: true);
  }

  void _handleTypeChanged(BarcodePdfType type) {
    setState(() {
      _type = type;
      _items.replaceRange(
        0,
        _items.length,
        _items.map((item) {
          if (!item.autoGenerated) return item;
          return item.copyWith(
            barcodeValue: _generateBarcode(),
            autoGenerated: true,
          );
        }),
      );
    });
  }

  String? _validateItems() {
    if (_items.isEmpty) return 'ยังไม่มีสินค้าในรายการ';
    final invalid = _items
        .where((i) => i.name.trim().isEmpty || i.barcodeValue.trim().isEmpty)
        .toList();
    if (invalid.isNotEmpty) return 'กรุณากรอกชื่อสินค้าและค่าบาร์โค้ดให้ครบ';
    if (_type == BarcodePdfType.ean13) {
      final bad = _items
          .where((i) => !RegExp(r'^\d{12,13}$').hasMatch(i.barcodeValue.trim()))
          .toList();
      if (bad.isNotEmpty) {
        return 'EAN-13 ต้องเป็นตัวเลข 12–13 หลัก (${bad.first.name.isEmpty ? bad.first.barcodeValue : bad.first.name})';
      }
    }
    return null;
  }

  List<BarcodeLabel> _buildLabels() => _items
      .expand(
        (item) => BarcodePdfBuilder.expand(
          BarcodeLabel(
            name: item.name.trim(),
            barcodeValue: item.barcodeValue.trim(),
          ),
          item.qty,
        ),
      )
      .toList();

  Future<void> _preview() async {
    final err = _validateItems();
    if (err != null) {
      _snack(err, error: true);
      return;
    }

    setState(() => _isPreviewing = true);
    try {
      await _persistCurrentItemsToHistory();
      final doc = await BarcodePdfBuilder.build(
        _buildLabels(),
        columnsPerRow: _columnsPerRow,
        type: _type,
      );
      final bytes = await doc.save();
      if (!mounted) return;
      await showDialog<void>(
        context: context,
        builder: (_) => PdfPreviewDialog(
          bytes: bytes,
          title: 'ตัวอย่างบาร์โค้ด',
          filename: 'barcodes_${DateTime.now().millisecondsSinceEpoch}.pdf',
        ),
      );
    } catch (e) {
      if (mounted) _snack('สร้าง PDF ไม่สำเร็จ: $e', error: true);
    } finally {
      if (mounted) setState(() => _isPreviewing = false);
    }
  }

  Future<void> _print() async {
    final err = _validateItems();
    if (err != null) {
      _snack(err, error: true);
      return;
    }

    setState(() => _isPrinting = true);
    try {
      await _persistCurrentItemsToHistory();
      final labels = _buildLabels();
      await Printing.layoutPdf(
        onLayout: (_) async {
          final doc = await BarcodePdfBuilder.build(
            labels,
            columnsPerRow: _columnsPerRow,
            type: _type,
          );
          return doc.save();
        },
        name: 'barcodes_${DateTime.now().millisecondsSinceEpoch}',
      );
    } catch (e) {
      if (mounted) _snack('พิมพ์ไม่สำเร็จ: $e', error: true);
    } finally {
      if (mounted) setState(() => _isPrinting = false);
    }
  }

  Future<void> _share() async {
    final err = _validateItems();
    if (err != null) {
      _snack(err, error: true);
      return;
    }

    setState(() => _isSharing = true);
    try {
      await _persistCurrentItemsToHistory();
      final doc = await BarcodePdfBuilder.build(
        _buildLabels(),
        columnsPerRow: _columnsPerRow,
        type: _type,
      );
      final bytes = await doc.save();
      await Printing.sharePdf(
        bytes: bytes,
        filename: 'barcodes_${DateTime.now().millisecondsSinceEpoch}.pdf',
      );
    } catch (e) {
      if (mounted) _snack('แชร์ไม่สำเร็จ: $e', error: true);
    } finally {
      if (mounted) setState(() => _isSharing = false);
    }
  }

  String _generateBarcode() {
    final now = DateTime.now();
    final base =
        '${now.year.toString().substring(2)}'
        '${now.month.toString().padLeft(2, '0')}'
        '${now.day.toString().padLeft(2, '0')}'
        '${now.hour.toString().padLeft(2, '0')}'
        '${now.minute.toString().padLeft(2, '0')}'
        '${now.second.toString().padLeft(2, '0')}';
    final millis = (now.millisecond + _items.length).toString().padLeft(3, '0');
    final body12 = '$base$millis'.substring(0, 12);
    return '$body12${_ean13CheckDigit(body12)}';
  }

  int _ean13CheckDigit(String body12) {
    final digits = body12.split('').map(int.parse).toList();
    int sum = 0;
    for (int i = 0; i < digits.length; i++) {
      sum += digits[i] * (i.isEven ? 1 : 3);
    }
    return (10 - (sum % 10)) % 10;
  }

  String _barcodeTypeKey(BarcodePdfType type) {
    switch (type) {
      case BarcodePdfType.code128:
        return 'code128';
      case BarcodePdfType.ean13:
        return 'ean13';
      case BarcodePdfType.qrCode:
        return 'qrcode';
    }
  }

  void _snack(String msg, {bool error = false}) {
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(
        content: Text(msg),
        behavior: SnackBarBehavior.floating,
        backgroundColor: error ? AppTheme.error : AppTheme.success,
      ),
    );
  }

  @override
  Widget build(BuildContext context) {
    final products = ref.watch(productListProvider).value ?? [];
    final colors = _BarcodePageColors.of(context);
    final isWide = MediaQuery.of(context).size.width >= 1100;

    return Scaffold(
      backgroundColor: colors.scaffoldBg,
      body: Column(
        children: [
          _BarcodeTopBar(
            colors: colors,
            totalSlips: _totalSlips,
            totalItems: _items.length,
            isPrinting: _isPrinting,
            onPrint: _items.isEmpty ? null : _print,
          ),
          _BarcodeSummaryBar(
            colors: colors,
            totalItems: _items.length,
            totalSlips: _totalSlips,
            historyCount: _history.length,
            typeLabel: _type.label,
          ),
          Expanded(
            child: Padding(
              padding: const EdgeInsets.fromLTRB(16, 16, 16, 0),
              child: isWide
                  ? Row(
                      crossAxisAlignment: CrossAxisAlignment.stretch,
                      children: [
                        Expanded(
                          flex: 7,
                          child: _BarcodeMainPanel(
                            colors: colors,
                            type: _type,
                            columnsPerRow: _columnsPerRow,
                            onTypeChanged: _handleTypeChanged,
                            onColumnsChanged: (c) =>
                                setState(() => _columnsPerRow = c),
                            child: _buildItemsList(colors),
                          ),
                        ),
                        const SizedBox(width: 16),
                        SizedBox(
                          width: 320,
                          child: _BarcodeHistoryPanel(
                            colors: colors,
                            entries: _filteredHistory,
                            query: _historyQuery,
                            isLoading: _isHistoryLoading,
                            onQueryChanged: (value) =>
                                setState(() => _historyQuery = value),
                            onUse: _addFromHistory,
                            onRemove: _removeHistoryEntry,
                            onClear: _clearHistory,
                          ),
                        ),
                      ],
                    )
                  : Column(
                      children: [
                        Expanded(
                          flex: 6,
                          child: _BarcodeMainPanel(
                            colors: colors,
                            type: _type,
                            columnsPerRow: _columnsPerRow,
                            onTypeChanged: _handleTypeChanged,
                            onColumnsChanged: (c) =>
                                setState(() => _columnsPerRow = c),
                            child: _buildItemsList(colors),
                          ),
                        ),
                        const SizedBox(height: 12),
                        Expanded(
                          flex: 4,
                          child: _BarcodeHistoryPanel(
                            colors: colors,
                            entries: _filteredHistory,
                            query: _historyQuery,
                            isLoading: _isHistoryLoading,
                            onQueryChanged: (value) =>
                                setState(() => _historyQuery = value),
                            onUse: _addFromHistory,
                            onRemove: _removeHistoryEntry,
                            onClear: _clearHistory,
                          ),
                        ),
                      ],
                    ),
            ),
          ),
        ],
      ),
      bottomNavigationBar: _BottomBar(
        colors: colors,
        products: products,
        isPreviewing: _isPreviewing,
        isSharing: _isSharing,
        hasItems: _items.isNotEmpty,
        onAddManual: () => _addItem(),
        onAddFromProduct: _addFromProduct,
        onPreview: _preview,
        onShare: _share,
      ),
    );
  }

  Widget _buildItemsList(_BarcodePageColors colors) {
    if (_items.isEmpty) {
      return _EmptyState(colors: colors, onAdd: () => _addItem());
    }

    return ListView.separated(
      padding: const EdgeInsets.all(16),
      itemCount: _items.length,
      separatorBuilder: (_, _) => const SizedBox(height: 10),
      itemBuilder: (_, i) => _ItemRow(
        item: _items[i],
        colors: colors,
        isSelected: _selectedItemId == _items[i].id,
        onActivate: () => _selectItem(_items[i].id),
        onRemove: () => _removeItem(_items[i].id),
        onNameChanged: (v) => _updateItem(_items[i].id, name: v),
        onBarcodeChanged: (v) =>
            _updateItem(_items[i].id, barcodeValue: v, autoGenerated: false),
        onQtyChanged: (v) => _updateItem(_items[i].id, qty: v),
        onGenerateBarcode: () => _regenerateBarcode(_items[i].id),
      ),
    );
  }
}

