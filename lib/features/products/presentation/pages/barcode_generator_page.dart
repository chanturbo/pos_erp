class _ItemRowState extends State<_ItemRow> {
  late final TextEditingController _nameCtrl;
  late final TextEditingController _barcodeCtrl;
  late final TextEditingController _qtyCtrl;

  @override
  void initState() {
    super.initState();
    _nameCtrl = TextEditingController(text: widget.item.name);
    _barcodeCtrl = TextEditingController(text: widget.item.barcodeValue);
    _qtyCtrl = TextEditingController(text: widget.item.qty.toString());
  }

  @override
  void didUpdateWidget(covariant _ItemRow oldWidget) {
    super.didUpdateWidget(oldWidget);
    if (oldWidget.item.name != widget.item.name &&
        _nameCtrl.text != widget.item.name) {
      _nameCtrl.text = widget.item.name;
    }
    if (oldWidget.item.barcodeValue != widget.item.barcodeValue &&
        _barcodeCtrl.text != widget.item.barcodeValue) {
      _barcodeCtrl.text = widget.item.barcodeValue;
    }
    final qtyText = widget.item.qty.toString();
    if (oldWidget.item.qty != widget.item.qty && _qtyCtrl.text != qtyText) {
      _qtyCtrl.text = qtyText;
    }
  }

  @override
  void dispose() {
    _nameCtrl.dispose();
    _barcodeCtrl.dispose();
    _qtyCtrl.dispose();
    super.dispose();
  }

  void _stepQty(int delta) {
    final v = int.tryParse(_qtyCtrl.text) ?? 1;
    final next = (v + delta).clamp(1, 999);
    _qtyCtrl.text = next.toString();
    widget.onQtyChanged(next);
  }

  @override
  Widget build(BuildContext context) {
    return Card(
      elevation: 0,
      margin: EdgeInsets.zero,
      color: widget.colors.cardBg,
      shape: RoundedRectangleBorder(
        borderRadius: BorderRadius.circular(12),
        side: BorderSide(
          color: widget.isSelected ? AppTheme.primary : widget.colors.border,
          width: widget.isSelected ? 1.4 : 1,
        ),
      ),
      child: InkWell(
        onTap: widget.onActivate,
        borderRadius: BorderRadius.circular(12),
        child: Padding(
          padding: const EdgeInsets.symmetric(horizontal: 10, vertical: 8),
          child: SingleChildScrollView(
            scrollDirection: Axis.horizontal,
            child: ConstrainedBox(
              constraints: const BoxConstraints(minWidth: 760),
              child: Row(
                children: [
                  Container(
                    width: 30,
                    height: 30,
                    decoration: BoxDecoration(
                      color: widget.isSelected
                          ? AppTheme.primary.withValues(alpha: 0.16)
                          : AppTheme.primary.withValues(alpha: 0.1),
                      borderRadius: BorderRadius.circular(8),
                    ),
                    child: Icon(
                      widget.isSelected
                          ? Icons.radio_button_checked
                          : Icons.inventory_2_outlined,
                      size: 16,
                      color: AppTheme.primary,
                    ),
                  ),
                  const SizedBox(width: 8),
                  SizedBox(width: 220, child: _buildNameField()),
                  const SizedBox(width: 8),
                  SizedBox(width: 260, child: _buildBarcodeField()),
                  const SizedBox(width: 8),
                  SizedBox(width: 120, child: _buildQtyField()),
                  const SizedBox(width: 8),
                  if (widget.item.autoGenerated)
                    Container(
                      padding: const EdgeInsets.symmetric(
                        horizontal: 8,
                        vertical: 3,
                      ),
                      decoration: BoxDecoration(
                        color: AppTheme.info.withValues(alpha: 0.1),
                        borderRadius: BorderRadius.circular(999),
                      ),
                      child: const Text(
                        'AUTO',
                        style: TextStyle(
                          fontSize: 9,
                          fontWeight: FontWeight.w700,
                          color: AppTheme.info,
                        ),
                      ),
                    )
                  else
                    Text(
                      '${widget.item.qty} สลิป',
                      style: TextStyle(
                        fontSize: 10,
                        color: widget.colors.subtext,
                      ),
                    ),
                  const SizedBox(width: 4),
                  IconButton(
                    visualDensity: VisualDensity.compact,
                    icon: const Icon(
                      Icons.delete_outline,
                      color: AppTheme.error,
                      size: 18,
                    ),
                    tooltip: 'ลบรายการนี้',
                    onPressed: widget.onRemove,
                  ),
                ],
              ),
            ),
          ),
        ),
      ),
    );
  }

  Widget _buildNameField() {
    return TextField(
      controller: _nameCtrl,
      onTap: widget.onActivate,
      decoration: const InputDecoration(
        labelText: 'ชื่อสินค้า',
        isDense: true,
        border: OutlineInputBorder(),
        contentPadding: EdgeInsets.symmetric(horizontal: 10, vertical: 9),
        labelStyle: TextStyle(fontSize: 11),
      ),
      style: const TextStyle(fontSize: 13, fontWeight: FontWeight.w500),
      onChanged: widget.onNameChanged,
    );
  }

  Widget _buildBarcodeField() {
    return TextField(
      controller: _barcodeCtrl,
      onTap: widget.onActivate,
      decoration: InputDecoration(
        labelText: 'ค่าบาร์โค้ด',
        isDense: true,
        border: const OutlineInputBorder(),
        contentPadding: const EdgeInsets.symmetric(horizontal: 10, vertical: 9),
        labelStyle: const TextStyle(fontSize: 11),
        suffixIcon: IconButton(
          tooltip: 'สร้างอัตโนมัติ',
          onPressed: widget.onGenerateBarcode,
          icon: const Icon(Icons.auto_awesome_outlined, size: 16),
        ),
      ),
      style: TextStyle(fontSize: 13, color: widget.colors.subtext),
      onChanged: widget.onBarcodeChanged,
    );
  }

  Widget _buildQtyField() {
    return Row(
      children: [
        _StepBtn(icon: Icons.remove, onTap: () => _stepQty(-1)),
        Expanded(
          child: TextField(
            controller: _qtyCtrl,
            onTap: widget.onActivate,
            textAlign: TextAlign.center,
            keyboardType: TextInputType.number,
            inputFormatters: [FilteringTextInputFormatter.digitsOnly],
            decoration: const InputDecoration(
              isDense: true,
              border: OutlineInputBorder(),
              hintText: 'Qty',
              hintStyle: TextStyle(fontSize: 11),
              contentPadding: EdgeInsets.symmetric(horizontal: 8, vertical: 9),
            ),
            style: const TextStyle(fontSize: 13, fontWeight: FontWeight.w600),
            onChanged: (v) {
              final n = int.tryParse(v);
              if (n != null && n > 0) widget.onQtyChanged(n);
            },
          ),
        ),
        _StepBtn(icon: Icons.add, onTap: () => _stepQty(1)),
      ],
    );
  }
}

