class _AddFromProductBtn extends StatefulWidget {
  final List<ProductModel> products;
  final ValueChanged<ProductModel> onSelected;

  const _AddFromProductBtn({required this.products, required this.onSelected});

  @override
  State<_AddFromProductBtn> createState() => _AddFromProductBtnState();
}

class _AddFromProductBtnState extends State<_AddFromProductBtn> {
  final _searchCtrl = TextEditingController();
  String _query = '';

  @override
  void dispose() {
    _searchCtrl.dispose();
    super.dispose();
  }

  void _show() {
    _searchCtrl.clear();
    setState(() => _query = '');
    showModalBottomSheet<ProductModel>(
      context: context,
      isScrollControlled: true,
      shape: const RoundedRectangleBorder(
        borderRadius: BorderRadius.vertical(top: Radius.circular(16)),
      ),
      builder: (ctx) => StatefulBuilder(
        builder: (ctx, setModal) => DraggableScrollableSheet(
          expand: false,
          initialChildSize: 0.65,
          maxChildSize: 0.92,
          builder: (_, sc) => Column(
            children: [
              Container(
                width: 42,
                height: 4,
                margin: const EdgeInsets.symmetric(vertical: 10),
                decoration: BoxDecoration(
                  color: Colors.grey.shade400,
                  borderRadius: BorderRadius.circular(2),
                ),
              ),
              Padding(
                padding: const EdgeInsets.fromLTRB(16, 0, 16, 8),
                child: TextField(
                  controller: _searchCtrl,
                  autofocus: true,
                  decoration: const InputDecoration(
                    hintText: 'ค้นหาสินค้า...',
                    prefixIcon: Icon(Icons.search),
                    border: OutlineInputBorder(),
                    isDense: true,
                  ),
                  onChanged: (v) => setModal(() => _query = v.toLowerCase()),
                ),
              ),
              Expanded(
                child: ListView.separated(
                  controller: sc,
                  itemCount: _filtered.length,
                  separatorBuilder: (_, _) => const Divider(height: 1),
                  itemBuilder: (_, i) {
                    final p = _filtered[i];
                    return ListTile(
                      dense: true,
                      leading: const Icon(Icons.inventory_2_outlined, size: 20),
                      title: Text(
                        p.productName,
                        style: const TextStyle(fontSize: 13),
                      ),
                      subtitle: Text(
                        '${p.productCode}${p.barcode != null ? " • ${p.barcode}" : ""}',
                        style: const TextStyle(fontSize: 11),
                      ),
                      onTap: () {
                        Navigator.pop(ctx);
                        widget.onSelected(p);
                      },
                    );
                  },
                ),
              ),
            ],
          ),
        ),
      ),
    );
  }

  List<ProductModel> get _filtered {
    if (_query.isEmpty) return widget.products;
    return widget.products.where((p) {
      return p.productName.toLowerCase().contains(_query) ||
          p.productCode.toLowerCase().contains(_query) ||
          (p.barcode?.toLowerCase().contains(_query) ?? false);
    }).toList();
  }

  @override
  Widget build(BuildContext context) {
    return FilledButton.tonalIcon(
      onPressed: _show,
      icon: const Icon(Icons.inventory_2_outlined, size: 16),
      label: const Text('เลือกจากสินค้า'),
    );
  }
}

class _BarcodePageColors {
  final bool isDark;
  final Color scaffoldBg;
  final Color cardBg;
  final Color border;
  final Color text;
  final Color subtext;
  final Color topBarBg;
  final Color summaryBg;
  final Color emptyIcon;

  const _BarcodePageColors({
    required this.isDark,
    required this.scaffoldBg,
    required this.cardBg,
    required this.border,
    required this.text,
    required this.subtext,
    required this.topBarBg,
    required this.summaryBg,
    required this.emptyIcon,
  });

  factory _BarcodePageColors.of(BuildContext context) {
    final isDark = Theme.of(context).brightness == Brightness.dark;
    return _BarcodePageColors(
      isDark: isDark,
      scaffoldBg: isDark ? AppTheme.darkBg : AppTheme.surface,
      cardBg: isDark ? const Color(0xFF2C2C2C) : Colors.white,
      border: isDark ? const Color(0xFF333333) : AppTheme.border,
      text: isDark ? const Color(0xFFE0E0E0) : const Color(0xFF1A1A1A),
      subtext: isDark ? const Color(0xFF9E9E9E) : AppTheme.textSub,
      topBarBg: isDark ? AppTheme.navyDark : AppTheme.navy,
      summaryBg: isDark ? const Color(0xFF181818) : const Color(0xFFFFF8F5),
      emptyIcon: isDark ? const Color(0xFF9E9E9E) : Colors.grey,
    );
  }
}

extension on BarcodePdfType {
  String get label {
    switch (this) {
      case BarcodePdfType.code128:
        return 'Code128';
      case BarcodePdfType.ean13:
        return 'EAN-13';
      case BarcodePdfType.qrCode:
        return 'QR';
    }
  }
}
