// ─────────────────────────────────────────────────────────────────
// ProductFormPage
// ─────────────────────────────────────────────────────────────────
class ProductFormPage extends ConsumerStatefulWidget {
  final ProductModel? product;
  const ProductFormPage({super.key, this.product});

  @override
  ConsumerState<ProductFormPage> createState() => _ProductFormPageState();
}

class _ProductFormPageState extends ConsumerState<ProductFormPage> {
  final _formKey = GlobalKey<FormState>();

  late final TextEditingController _codeController;
  late final TextEditingController _nameController;
  late final TextEditingController _barcodeController;
  late final TextEditingController _unitController;
  late final TextEditingController _price1Controller;
  late final TextEditingController _price2Controller;
  late final TextEditingController _price3Controller;
  late final TextEditingController _price4Controller;
  late final TextEditingController _price5Controller;
  late final TextEditingController _costController;

  bool _isStockControl    = true;
  bool _allowNegativeStock = false;
  bool _isLoading          = false;
  bool _codeAutoGenerated  = false; // ✅ ติดตามว่ารหัสถูก generate อัตโนมัติ

  String? _imagePath;

  @override
  void initState() {
    super.initState();
    final p = widget.product;
    _codeController    = TextEditingController(text: p?.productCode ?? '');
    _nameController    = TextEditingController(text: p?.productName ?? '');
    _barcodeController = TextEditingController(text: p?.barcode ?? '');
    _unitController    = TextEditingController(text: p?.baseUnit ?? 'ชิ้น');
    _price1Controller  = TextEditingController(text: p?.priceLevel1.toString() ?? '0');
    _price2Controller  = TextEditingController(text: p?.priceLevel2.toString() ?? '0');
    _price3Controller  = TextEditingController(text: p?.priceLevel3.toString() ?? '0');
    _price4Controller  = TextEditingController(text: p?.priceLevel4.toString() ?? '0');
    _price5Controller  = TextEditingController(text: p?.priceLevel5.toString() ?? '0');
    _costController    = TextEditingController(text: p?.standardCost.toString() ?? '0');

    if (p != null) {
      _isStockControl     = p.isStockControl;
      _allowNegativeStock = p.allowNegativeStock;
      _imagePath          = p.imagePath;
    }

    // ✅ เมื่อ barcode เปลี่ยน → อัปเดตรหัสสินค้า (เฉพาะ create mode)
    if (widget.product == null) {
      _barcodeController.addListener(_onBarcodeChanged);
    }
  }

  // ─────────────────────────────────────────────────────────────
  // Auto-generate product code
  // ─────────────────────────────────────────────────────────────

  /// เรียกเมื่อ barcode field เปลี่ยน (create mode เท่านั้น)
  void _onBarcodeChanged() {
    final barcode = _barcodeController.text.trim();
    // ถ้า user ยังไม่ได้แก้รหัสเอง หรือรหัสเป็น auto-generated ก่อนหน้า
    if (_codeAutoGenerated || _codeController.text.isEmpty) {
      if (barcode.isNotEmpty) {
        // ใช้ barcode เป็นรหัสสินค้า
        _codeController.text = barcode;
      } else {
        // barcode ว่าง → generate รหัสจาก timestamp
        _codeController.text = _generateCode();
      }
      _codeAutoGenerated = true;
    }
  }

  /// สร้างรหัสสินค้าอัตโนมัติ เช่น PRD-250321-001
  String _generateCode() {
    final now = DateTime.now();
    final date =
        '${now.year.toString().substring(2)}${now.month.toString().padLeft(2, '0')}${now.day.toString().padLeft(2, '0')}';
    final seq = (now.millisecondsSinceEpoch % 1000).toString().padLeft(3, '0');
    return 'PRD-$date-$seq';
  }

  /// กด "สร้างรหัสอัตโนมัติ" ด้วยตนเอง
  void _autoGenerateCode() {
    final barcode = _barcodeController.text.trim();
    setState(() {
      _codeController.text =
          barcode.isNotEmpty ? barcode : _generateCode();
      _codeAutoGenerated = true;
    });
  }

  @override
  void dispose() {
    _barcodeController.removeListener(_onBarcodeChanged);
    _codeController.dispose();
    _nameController.dispose();
    _barcodeController.dispose();
    _unitController.dispose();
    _price1Controller.dispose();
    _price2Controller.dispose();
    _price3Controller.dispose();
    _price4Controller.dispose();
    _price5Controller.dispose();
    _costController.dispose();
    super.dispose();
  }

  // ─────────────────────────────────────────────────────────────
  // Image picker
  // ─────────────────────────────────────────────────────────────
  Future<void> _pickImage() async {
    final result = await FilePicker.platform.pickFiles(
      type: FileType.image,
      allowMultiple: false,
    );
    if (result == null || result.files.isEmpty) return;
    final path = result.files.first.path;
    if (path == null) return;
    setState(() => _imagePath = path);
  }

  void _removeImage() => setState(() => _imagePath = null);

  // ─────────────────────────────────────────────────────────────
  // Submit
  // ─────────────────────────────────────────────────────────────
  Future<void> _handleSubmit() async {
    if (!_formKey.currentState!.validate()) return;
    setState(() => _isLoading = true);

    double d(TextEditingController c) =>
        double.tryParse(c.text.isEmpty ? '0' : c.text) ?? 0;

    final data = {
      'product_code': _codeController.text.trim(),
      'product_name': _nameController.text.trim(),
      'barcode': _barcodeController.text.trim().isEmpty
          ? null
          : _barcodeController.text.trim(),
      'base_unit': _unitController.text.trim(),
      'price_level1': d(_price1Controller),
      'price_level2': d(_price2Controller),
      'price_level3': d(_price3Controller),
      'price_level4': d(_price4Controller),
      'price_level5': d(_price5Controller),
      'standard_cost': d(_costController),
      'is_stock_control': _isStockControl,
      'allow_negative_stock': _allowNegativeStock,
      'image_path': _imagePath,
    };

    final bool success;
    if (widget.product != null) {
      success = await ref
          .read(productListProvider.notifier)
          .updateProduct(widget.product!.productId, data);
    } else {
      success =
          await ref.read(productListProvider.notifier).addProduct(data);
    }

    if (mounted) {
      setState(() => _isLoading = false);
      if (success) {
        Navigator.pop(context);
        ScaffoldMessenger.of(context).showSnackBar(SnackBar(
          content: Text(widget.product != null
              ? 'แก้ไขสินค้าสำเร็จ'
              : 'เพิ่มสินค้าสำเร็จ'),
          backgroundColor: AppTheme.successColor,
        ));
      } else {
        ScaffoldMessenger.of(context).showSnackBar(const SnackBar(
          content: Text('เกิดข้อผิดพลาด กรุณาลองใหม่'),
          backgroundColor: AppTheme.errorColor,
        ));
      }
    }
  }

  // ─────────────────────────────────────────────────────────────
  // Build
  // ─────────────────────────────────────────────────────────────
  @override
  Widget build(BuildContext context) {
    final isEdit = widget.product != null;

    return Scaffold(
      backgroundColor: Theme.of(context).brightness == Brightness.dark
          ? AppTheme.darkBg
          : const Color(0xFFF5F5F5),
      body: Column(
        children: [
          // ── Title Bar ─────────────────────────────────────────
          _TitleBar(isEdit: isEdit),

          // ── Form Body ─────────────────────────────────────────
          Expanded(
            child: Form(
              key: _formKey,
              child: SingleChildScrollView(
                padding: const EdgeInsets.all(20),
                child: Column(
                  children: [
                    // ══ Row 1 (Responsive) ════════════════════
                    LayoutBuilder(builder: (ctx, bc) {
                      final isWide = bc.maxWidth >= 880;
                      final c1 = _SectionCard(
                              icon: Icons.inventory_2_outlined,
                              iconColor: AppTheme.primary,
                              title: 'ข้อมูลสินค้า',
                              child: Column(
                                children: [
                                  // ① บาร์โค้ด (ขึ้นก่อน)
                                  _BarcodeField(
                                    controller: _barcodeController,
                                    enabled: !_isLoading,
                                    onScanned: (val) {
                                      setState(() {
                                        _barcodeController.text = val;
                                        // trigger listener
                                        _onBarcodeChanged();
                                      });
                                    },
                                  ),
                                  const SizedBox(height: 14),

                                  // ② รหัสสินค้า (auto จาก barcode)
                                  _ProductCodeField(
                                    controller: _codeController,
                                    isEdit: isEdit,
                                    isAutoGenerated: _codeAutoGenerated,
                                    enabled: !_isLoading,
                                    onAutoGenerate: _autoGenerateCode,
                                    onManualEdit: () =>
                                        setState(() => _codeAutoGenerated = false),
                                  ),
                                  const SizedBox(height: 14),

                                  // ③ ชื่อสินค้า
                                  _FormField(
                                    controller: _nameController,
                                    label: 'ชื่อสินค้า',
                                    icon: Icons.label_outlined,
                                    required: true,
                                    enabled: !_isLoading,
                                    validator: (v) => (v == null || v.isEmpty)
                                        ? 'กรุณากรอกชื่อสินค้า'
                                        : null,
                                  ),
                                  const SizedBox(height: 14),

                                  // ④ หน่วย
                                  _FormField(
                                    controller: _unitController,
                                    label: 'หน่วย',
                                    icon: Icons.straighten_outlined,
                                    required: true,
                                    enabled: !_isLoading,
                                    validator: (v) => (v == null || v.isEmpty)
                                        ? 'กรุณากรอกหน่วย'
                                        : null,
                                  ),
                                ],
                              ),
                            );
                      final c2 = _SectionCard(
                              icon: Icons.sell_outlined,
                              iconColor: AppTheme.info,
                              title: 'ราคาขาย',
                              child: Column(
                                children: [
                                  // ── Level 1 — ราคาปกติ (required) ────
                                  _FormField(
                                    controller: _price1Controller,
                                    label: 'Level 1 — ราคาปกติ / ลูกค้าทั่วไป',
                                    icon: Icons.monetization_on_outlined,
                                    required: true,
                                    keyboardType: TextInputType.number,
                                    enabled: !_isLoading,
                                    prefixText: '฿',                                    
                                    validator: (v) => (v == null || v.isEmpty)
                                        ? 'กรุณากรอกราคา'
                                        : null,
                                  ),
                                  const SizedBox(height: 14),
                                  // ── Level 2 & 3 ───────────────────────
                                  _PriceRow(
                                    c1: _price2Controller,
                                    l1: 'Level 2',
                                    h1: 'ราคาสมาชิก',
                                    c2: _price3Controller,
                                    l2: 'Level 3',
                                    h2: 'ราคาส่ง',
                                    enabled: !_isLoading,
                                  ),
                                  const SizedBox(height: 10),
                                  // ── Level 4 & 5 ───────────────────────
                                  _PriceRow(
                                    c1: _price4Controller,
                                    l1: 'Level 4',
                                    h1: 'ราคาตัวแทน',
                                    c2: _price5Controller,
                                    l2: 'Level 5',
                                    h2: 'ราคา VIP / พิเศษ',
                                    enabled: !_isLoading,
                                  ),
                                  const SizedBox(height: 14),
                                  // ── ต้นทุน ────────────────────────────
                                  _FormField(
                                    controller: _costController,
                                    label: 'ต้นทุนมาตรฐาน',
                                    icon: Icons.price_change_outlined,
                                    keyboardType: TextInputType.number,
                                    enabled: !_isLoading,
                                    prefixText: '฿',
                                    helperText: 'ใช้คำนวณกำไรขั้นต้น',
                                  ),
                                ],
                              ),
                            );
                      final c3 = Column(
                              children: [
                                // รูปสินค้า
                                _SectionCard(
                                  icon: Icons.photo_outlined,
                                  iconColor: const Color(0xFF7B1FA2),
                                  title: 'รูปสินค้า',
                                  child: _ImagePickerWidget(
                                    imagePath: _imagePath,
                                    onPick: _pickImage,
                                    onRemove: _removeImage,
                                  ),
                                ),
                                const SizedBox(height: 16),

                                // ตั้งค่าสต๊อก
                                _SectionCard(
                                  icon: Icons.warehouse_outlined,
                                  iconColor: AppTheme.success,
                                  title: 'ตั้งค่าสต๊อก',
                                  child: Column(
                                    children: [
                                      _SwitchRow(
                                        label: 'ควบคุมสต๊อก',
                                        subLabel: 'ติดตามการเคลื่อนไหว',
                                        value: _isStockControl,
                                        activeColor: AppTheme.success,
                                        onChanged: (v) => setState(
                                            () => _isStockControl = v),
                                      ),
                                      if (_isStockControl) ...[
                                        const Divider(
                                            height: 16, color: AppTheme.border),
                                        _SwitchRow(
                                          label: 'อนุญาตติดลบ',
                                          subLabel: 'ขายได้แม้สต๊อกหมด',
