// ─────────────────────────────────────────────────────────────────
// Image Picker Widget
// ─────────────────────────────────────────────────────────────────
class _ImagePickerWidget extends StatelessWidget {
  final String? imagePath;
  final VoidCallback onPick;
  final VoidCallback onRemove;

  const _ImagePickerWidget({
    required this.imagePath,
    required this.onPick,
    required this.onRemove,
  });

  @override
  Widget build(BuildContext context) {
    final hasImage   = imagePath != null && imagePath!.isNotEmpty;
    final file       = hasImage ? File(imagePath!) : null;
    final fileExists = file != null && file.existsSync();

    return Column(
      children: [
        // Preview
        GestureDetector(
          onTap: onPick,
          child: Container(
            width: double.infinity,
            height: 120,
            decoration: BoxDecoration(
              color: hasImage && fileExists
                  ? AppTheme.primaryLight
                  : Colors.grey[400],
              borderRadius: BorderRadius.circular(10),
              border: Border.all(
                color: hasImage && fileExists
                    ? AppTheme.primary.withValues(alpha: 0.4)
                    : Colors.grey[400]!,
                width: hasImage && fileExists ? 1.5 : 1,
              ),
            ),
            child: hasImage && fileExists
                ? ClipRRect(
                    borderRadius: BorderRadius.circular(9),
                    child: Image.file(file,
                        fit: BoxFit.cover,
                        errorBuilder: (_, e, s) => _placeholder()),
                  )
                : _placeholder(),
          ),
        ),
        const SizedBox(height: 10),

        // Buttons
        Row(
          children: [
            Expanded(
              child: ElevatedButton.icon(
                onPressed: onPick,
                icon: const Icon(Icons.photo_library_outlined, size: 15),
                label: Text(hasImage ? 'เปลี่ยนรูป' : 'เลือกรูป',
                    style: const TextStyle(fontSize: 12)),
                style: ElevatedButton.styleFrom(
                  backgroundColor: AppTheme.primary,
                  foregroundColor: Colors.white,
                  padding: const EdgeInsets.symmetric(vertical: 8),
                  shape: RoundedRectangleBorder(
                      borderRadius: BorderRadius.circular(8)),
                  elevation: 0,
                ),
              ),
            ),
            if (hasImage) ...[
              const SizedBox(width: 8),
              OutlinedButton(
                onPressed: onRemove,
                style: OutlinedButton.styleFrom(
                  padding: const EdgeInsets.symmetric(
                      vertical: 8, horizontal: 12),
                  side: const BorderSide(color: Colors.red),
                  shape: RoundedRectangleBorder(
                      borderRadius: BorderRadius.circular(8)),
                ),
                child: const Icon(Icons.delete_outline,
                    size: 16, color: Colors.red),
              ),
            ],
          ],
        ),
        const SizedBox(height: 6),
        const Text('JPG, PNG, WEBP',
            style: TextStyle(fontSize: 10, color: AppTheme.textSub)),
      ],
    );
  }

  Widget _placeholder() => const Column(
        mainAxisAlignment: MainAxisAlignment.center,
        children: [
          Icon(Icons.add_photo_alternate_outlined,
              size: 32, color: Colors.white),
          SizedBox(height: 6),
          Text('กดเพื่อเลือกรูป',
              style: TextStyle(fontSize: 11, color: Colors.white)),
        ],
      );
}