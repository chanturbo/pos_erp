// ─────────────────────────────────────────────────────────────────
// Barcode Field — พร้อมปุ่มสแกน
// ─────────────────────────────────────────────────────────────────
class _BarcodeField extends StatelessWidget {
  final TextEditingController controller;
  final bool enabled;
  final void Function(String) onScanned;

  const _BarcodeField({
    required this.controller,
    required this.enabled,
    required this.onScanned,
  });

  @override
  Widget build(BuildContext context) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        // ── ปุ่มสแกน (ขึ้นก่อน) ──────────────────────────────
        SizedBox(
          width: double.infinity,
          child: ElevatedButton.icon(
            onPressed: enabled
                ? () async {
                    await MobileScannerService.openContinuous(
                      context,
                      onScanned: (result) {
                        Navigator.of(context).pop();
                        onScanned(result.value);
                      },
                    );
                  }
                : null,
            icon: const Icon(Icons.qr_code_scanner, size: 18),
            label: const Text('สแกนบาร์โค้ด',
                style: TextStyle(fontSize: 13)),
            style: ElevatedButton.styleFrom(
              backgroundColor: const Color(0xFF1565C0),
              foregroundColor: Colors.white,
              padding:
                  const EdgeInsets.symmetric(horizontal: 16, vertical: 10),
              shape: RoundedRectangleBorder(
                  borderRadius: BorderRadius.circular(8)),
              elevation: 0,
            ),
          ),
        ),
        const SizedBox(height: 8),

        // ── Text field บาร์โค้ด ──────────────────────────────
        TextFormField(
          controller: controller,
          enabled: enabled,
          style: const TextStyle(fontSize: 13),
          decoration: InputDecoration(
            labelText: 'บาร์โค้ด',
            labelStyle: const TextStyle(fontSize: 13, color: AppTheme.textSub),
            hintText: 'กรอกหรือสแกนบาร์โค้ด',
            hintStyle: const TextStyle(fontSize: 12, color: AppTheme.textSub),
            prefixIcon: const Icon(Icons.qr_code, size: 17, color: AppTheme.textSub),
            suffixIcon: IconButton(
              icon: const Icon(Icons.qr_code_scanner,
                  size: 18, color: AppTheme.info),
              tooltip: 'สแกน',
              onPressed: enabled
                  ? () async {
                      await MobileScannerService.openContinuous(
                        context,
                        onScanned: (result) {
                          Navigator.of(context).pop();
                          onScanned(result.value);
                        },
                      );
                    }
                  : null,
            ),
            contentPadding:
                const EdgeInsets.symmetric(horizontal: 12, vertical: 12),
            border: OutlineInputBorder(
                borderRadius: BorderRadius.circular(8),
                borderSide: const BorderSide(color: AppTheme.border)),
            enabledBorder: OutlineInputBorder(
                borderRadius: BorderRadius.circular(8),
                borderSide: const BorderSide(color: AppTheme.border)),
            focusedBorder: OutlineInputBorder(
                borderRadius: BorderRadius.circular(8),
                borderSide:
                    const BorderSide(color: AppTheme.primary, width: 1.5)),
            filled: true,
            fillColor: Theme.of(context).brightness == Brightness.dark
                ? AppTheme.darkElement
                : Colors.white,
          ),
        ),
      ],
    );
  }
}

