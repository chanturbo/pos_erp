class CustomerFormPage extends ConsumerStatefulWidget {
  final CustomerModel? customer;

  const CustomerFormPage({super.key, this.customer});

  @override
  ConsumerState<CustomerFormPage> createState() => _CustomerFormPageState();
}

class _CustomerFormPageState extends ConsumerState<CustomerFormPage> {
  final _formKey = GlobalKey<FormState>();
  late final TextEditingController _customerCodeController;
  late final TextEditingController _customerNameController;
  late final TextEditingController _addressController;
  late final TextEditingController _phoneController;
  late final TextEditingController _emailController;
  late final TextEditingController _taxIdController;
  late final TextEditingController _creditLimitController;
  late final TextEditingController _creditDaysController;
  late final TextEditingController _memberNoController;

  bool _isLoading        = false;
  bool _codeAutoGenerated = false; // ติดตามว่ารหัสถูก generate อัตโนมัติ
  int  _priceLevel       = 1;     // ระดับราคา 1-5
  bool _isActive         = true;  // สถานะการใช้งาน

  @override
  void initState() {
    super.initState();
    final c = widget.customer;
    _customerCodeController = TextEditingController(text: c?.customerCode ?? '');
    _customerNameController = TextEditingController(text: c?.customerName ?? '');
    _addressController      = TextEditingController(text: c?.address ?? '');
    _phoneController        = TextEditingController(text: c?.phone ?? '');
    _emailController        = TextEditingController(text: c?.email ?? '');
    _taxIdController        = TextEditingController(text: c?.taxId ?? '');
    _creditLimitController  =
        TextEditingController(text: c?.creditLimit.toString() ?? '0');
    _creditDaysController   =
        TextEditingController(text: c?.creditDays.toString() ?? '0');
    _memberNoController     = TextEditingController(text: c?.memberNo ?? '');

    // ✅ โหลด priceLevel และ isActive จากข้อมูลเดิม (ถ้ามี)
    _priceLevel = c?.priceLevel ?? 1;
    _isActive   = c?.isActive   ?? true;

    // สร้างรหัสอัตโนมัติเมื่อเพิ่มใหม่ + ยังไม่มีรหัส
    if (widget.customer == null && _customerCodeController.text.isEmpty) {
      _customerCodeController.text = _generateCode();
      _codeAutoGenerated = true;
    }

    // 🆕 Listener: ให้ปุ่ม "ใช้เบอร์โทร" โผล่/หายแบบ reactive
    _phoneController.addListener(() => setState(() {}));
  }

  // ── สร้างรหัสลูกค้าอัตโนมัติ เช่น CUS-250321-001 ──────────────
  String _generateCode() {
    final now  = DateTime.now();
    final date = '${now.year.toString().substring(2)}'
        '${now.month.toString().padLeft(2, '0')}'
        '${now.day.toString().padLeft(2, '0')}';
    final seq  = (now.millisecondsSinceEpoch % 1000).toString().padLeft(3, '0');
    return 'CUS-$date-$seq';
  }

  void _autoGenerateCode() {
    setState(() {
      _customerCodeController.text = _generateCode();
      _codeAutoGenerated = true;
    });
  }

  @override
  void dispose() {
    _customerCodeController.dispose();
    _customerNameController.dispose();
    _addressController.dispose();
    _phoneController.dispose();
    _emailController.dispose();
    _taxIdController.dispose();
    _creditLimitController.dispose();
    _creditDaysController.dispose();
    _memberNoController.dispose();
    super.dispose();
  }

  Future<void> _handleSave() async {
    if (!_formKey.currentState!.validate()) return;

    // ✅ ตรวจสอบเลขที่สมาชิกเมื่อเลือกระดับราคา > 1
    if (_priceLevel > 1 && _memberNoController.text.trim().isEmpty) {
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Row(
            children: [
              const Icon(Icons.warning_amber_rounded,
                  color: Colors.white, size: 18),
              const SizedBox(width: 8),
              Text(
                'ระดับราคา Level $_priceLevel '
                'ต้องระบุเลขที่สมาชิกด้วย',
              ),
            ],
          ),
          backgroundColor: Colors.orange[700],
          behavior: SnackBarBehavior.floating,
        ),
      );
      return; // หยุดบันทึก
    }

    setState(() => _isLoading = true);

    String? nul(String v) => v.trim().isEmpty ? null : v.trim();

    final data = {
      'customer_code': _customerCodeController.text.trim(),
      'customer_name': _customerNameController.text.trim(),
      'address': nul(_addressController.text),
      'phone': nul(_phoneController.text),
      'email': nul(_emailController.text),
      'tax_id': nul(_taxIdController.text),
      'credit_limit': double.tryParse(_creditLimitController.text) ?? 0,
      'credit_days': int.tryParse(_creditDaysController.text) ?? 0,
      'member_no': nul(_memberNoController.text),
      // ✅ ส่ง price_level ให้ server หา customer_group_id ที่ตรงกัน
      'price_level': _priceLevel,
      'is_active': _isActive,
      // ✅ ไม่ต้องส่ง customer_group_id เพราะ server จะ lookup จาก price_level
    };

    final bool success;
    if (widget.customer == null) {
      success =
          await ref.read(customerListProvider.notifier).createCustomer(data);
    } else {
      success = await ref
          .read(customerListProvider.notifier)
          .updateCustomer(widget.customer!.customerId, data);
    }

    setState(() => _isLoading = false);

    if (mounted) {
      if (success) {
        Navigator.pop(context);
        ScaffoldMessenger.of(context).showSnackBar(SnackBar(
          content: Text(widget.customer == null
              ? 'เพิ่มลูกค้าสำเร็จ'
              : 'แก้ไขลูกค้าสำเร็จ'),
          backgroundColor: Colors.green,
        ));
      } else {
        ScaffoldMessenger.of(context).showSnackBar(const SnackBar(
          content: Text('เกิดข้อผิดพลาด กรุณาลองใหม่'),
          backgroundColor: Colors.red,
        ));
      }
    }
  }

