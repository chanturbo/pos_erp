    (5, 'Level 5', 'ราคา VIP / พิเศษ',  Color(0xFFC62828), Color(0xFFFFEBEE)),
  ];

  @override
  Widget build(BuildContext context) {
    final isDark = Theme.of(context).brightness == Brightness.dark;

    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
