// ─────────────────────────────────────────────────────────────────
// _PriceLevelSelector — เลือกระดับราคา 1-5
// ─────────────────────────────────────────────────────────────────
class _PriceLevelSelector extends StatelessWidget {
  final int value;
  final bool enabled;
  final ValueChanged<int> onChanged;

  const _PriceLevelSelector({
    required this.value,
    required this.enabled,
    required this.onChanged,
  });

  static const _levels = [
    (1, 'Level 1', 'ราคาปกติ (ทั่วไป)',  Color(0xFF1565C0), Color(0xFFE3F2FD)),
    (2, 'Level 2', 'ราคาสมาชิก',         Color(0xFF2E7D32), Color(0xFFE8F5E9)),
    (3, 'Level 3', 'ราคาส่ง',             Color(0xFFE65100), Color(0xFFFFF3E0)),
    (4, 'Level 4', 'ราคาตัวแทน',         Color(0xFF6A1B9A), Color(0xFFF3E5F5)),
    (5, 'Level 5', 'ราคา VIP / พิเศษ',  Color(0xFFC62828), Color(0xFFFFEBEE)),
  ];

  @override
  Widget build(BuildContext context) {
    final isDark = Theme.of(context).brightness == Brightness.dark;

    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Row(
          children: [
            Icon(Icons.sell_outlined,
                size: 15,
                color: isDark ? Colors.white54 : AppColors.textSub),
            const SizedBox(width: 6),
            Text('ระดับราคา',
                style: TextStyle(
                    fontSize: 12,
                    fontWeight: FontWeight.w500,
                    color: isDark ? Colors.white70 : AppColors.textSub)),
          ],
        ),
        const SizedBox(height: 8),
        // ปุ่ม level 1-5 แบบ wrap
        Wrap(
          spacing: 8,
          runSpacing: 8,
          children: _levels.map((lvl) {
            final (level, label, desc, color, bgColor) = lvl;
            final isSelected = value == level;
            return GestureDetector(
              onTap: enabled ? () => onChanged(level) : null,
              child: AnimatedContainer(
                duration: const Duration(milliseconds: 150),
                padding: const EdgeInsets.symmetric(
                    horizontal: 12, vertical: 8),
                decoration: BoxDecoration(
                  color: isSelected
                      ? (isDark ? color.withValues(alpha: 0.3) : bgColor)
                      : (isDark ? AppColors.darkElement : Colors.white),
                  borderRadius: BorderRadius.circular(8),
                  border: Border.all(
                    color: isSelected
                        ? color
                        : (isDark ? Colors.white24 : AppColors.borderDark),
                    width: isSelected ? 1.5 : 1,
                  ),
                ),
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    Row(
                      mainAxisSize: MainAxisSize.min,
                      children: [
                        if (isSelected)
                          Icon(Icons.check_circle,
                              size: 13, color: color)
                        else
                          Icon(Icons.circle_outlined,
                              size: 13,
                              color: isDark
                                  ? Colors.white38
                                  : AppColors.textSub),
                        const SizedBox(width: 4),
                        Text(label,
                            style: TextStyle(
                                fontSize: 12,
                                fontWeight: FontWeight.w600,
                                color: isSelected
                                    ? color
                                    : (isDark
                                        ? Colors.white70
                                        : AppColors.textSub))),
                      ],
                    ),
                    const SizedBox(height: 2),
                    Text(desc,
                        style: TextStyle(
                            fontSize: 10,
                            color: isSelected
                                ? color.withValues(alpha: 0.8)
                                : (isDark
                                    ? Colors.white38
                                    : AppColors.textSub))),
                  ],
                ),
              ),
            );
          }).toList(),
        ),
        const SizedBox(height: 6),
        Text(
          'ราคาที่ใช้กับลูกค้ากลุ่มนี้เมื่อขายสินค้า',
          style: TextStyle(
              fontSize: 10,
              color: isDark ? Colors.white38 : AppColors.textSub),
        ),
      ],
    );
  }
}